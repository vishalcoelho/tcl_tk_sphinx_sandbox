"""
Build out several functions that will perform common math operations.
"""


def add(a: int, b: int) -> int:
    """
    Add two numbers.
    """
    return a + b


def sub(a: int, b: int) -> int:
    """
    Subtract two numbers.
    """
    return a - b


def mul(a: int, b: int) -> int:
    """
    Multiply two numbers.
    """
    return a * b


def div(a: int, b: int) -> int:
    """
    Divide two numbers.
    """
    return int(a / b)
